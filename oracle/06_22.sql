--8번
SELECT STUDENT_NO , STUDENT_NAME
FROM TB_STUDENT
WHERE SUBSTR(STUDENT_NO,1,1) !='A';

--9번
SELECT ROUND(AVG(POINT),1)
FROM TB_GRADE
WHERE STUDENT_NO = 'A517178';
--10번
SELECT DEPARTMENT_NO 학과번호 , COUNT(DEPARTMENT_NO) AS "학생수(명)"
FROM TB_STUDENT
GROUP BY DEPARTMENT_NO
ORDER BY 1;

--11번
SELECT COUNT(*)
FROM TB_STUDENT
WHERE COACH_PROFESSOR_NO IS NULL;

--12번 모르겠어요
SELECT  SUBSTR(TERM_NO,1,4),STUDENT_NAME,AVG(POINT)
FROM TB_GRADE
JOIN TB_STUDENT USING(STUDENT_NO)
WHERE DEPARTMENT_NO='A112113'  
GROUP BY TERM_NO,STUDENT_NAME;
--13번
SELECT DEPARTMENT_NO,COUNT(ABSENCE_YN)
FROM TB_STUDENT
WHERE ABSENCE_YN = 'N'
GROUP BY DEPARTMENT_NO
ORDER BY 1;
--14번
SELECT STUDENT_NAME, COUNT(STUDENT_NAME)
FROM TB_STUDENT
HAVING COUNT(STUDENT_NAME) >1
GROUP BY STUDENT_NAME
ORDER BY 1 ASC; 
--15번 모르겠어요
