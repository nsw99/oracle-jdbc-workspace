--1번
SELECT STUDENT_NO 학번 ,STUDENT_NAME 이름 ,TO_CHAR(ENTRANCE_DATE,'YYYY/MM/DD') 입학년도
FROM TB_STUDENT
WHERE DEPARTMENT_NO =002
ORDER BY 3;

--2번

SELECT PROFESSOR_NAME,PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME)!=3;

--3번

SELECT PROFESSOR_NAME 교수이름,  TO_CHAR(SYSDATE,'YYYY')- (1900+SUBSTR(PROFESSOR_SSN,0,2))
FROM TB_PROFESSOR;

--4번
SELECT  SUBSTR(PROFESSOR_NAME,2,2) 이름
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME)!=2 AND LENGTH(PROFESSOR_NAME)!=4;


--5번

SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE TO_CHAR(ENTRANCE_DATE,'YYYY')-(1900+SUBSTR(STUDENT_SSN,1,2)) =19
ORDER BY 1 DESC;

